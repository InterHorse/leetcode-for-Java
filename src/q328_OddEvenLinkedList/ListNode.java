package q328_OddEvenLinkedList;

/**
 * @author Yuzhe Ma
 * @date 2021/2/4
 */
public class ListNode {
    int val;
    ListNode next;

    ListNode() {
    }

    ListNode(int val) {
        this.val = val;
    }

    ListNode(int val, ListNode next) {
        this.val = val;
        this.next = next;
    }
}
